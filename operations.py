

def in_to_cm():
    inches = float(input("Enter length in Inches:"))
    cm = inches * 2.54
    print(f"{inches} Inches = {cm} Centimeters\n")

def ft_to_m():
    ft = float(input("Enter length in Feets:"))
    m = ft * 0.3048
    print(f"{ft} Feets = {m} Meters\n")

def yd_to_m():
    yards = float(input("Enter length in Yards:"))
    m = yards * 0.9144
    print(f"{yards} Yards = {m} Meters\n")

def miles_to_km():
    miles = float(input("Enter length in Miles:"))
    km = miles * 1.60934
    print(f"{miles} Miles = {km} Kilometers\n")

def sqin_to_sqcm():
    sqin = float(input("Enter area in Square Inches:"))
    sqcm = sqin * 6.4516
    print(f"{sqin} Square Inches = {sqcm} Square Centimeters\n")

def sqft_to_sqm():
    sqft = float(input("Enter area in Square Feets:"))
    sqm = sqft * 0.092903
    print(f"{sqft} Square Feets = {sqm} Square Meters\n")

def sqmile_to_sqkm():
    sqmile = float(input("Enter area in Square Miles:"))
    sqkm = sqmile * 2.58999
    print(f"{sqmile} Square Miles = {sqkm} Square Kilometers\n")

def acres_to_hectares():
    acres = float(input("Enter area in Acres:"))
    hectares = acres * 0.404686
    print(f"{acres} Acres = {hectares} Hectares\n")

def pounds_to_kg():
    pounds = float(input("Enter weight in Pounds:"))
    kg = pounds * 0.453592
    print(f"{pounds} Pounds = {kg} Kilograms\n")

def ounces_to_grams():
    ounces = float(input("Enter weight in Ounces:"))
    grams = ounces * 28.3495
    print(f"{ounces} Ounces = {grams} Grams\n")

